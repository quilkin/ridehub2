/**
 * helper class to check if a rider is 'already' signed up
 */
export class Already  {
    ridingToDest ='';   // already on that ride
    reservedToDest='';  // already on the reserved list for the ride
    leadingToDest= '';  // alreqdy a leader for the ride
    ridingOnDate= 0;    // already doing a (different) ride that day
    reservedOnDate= 0;  // already reserved on a (different) ride that day
    leadingOnDate= 0;   // already leading a (different) ride that day
  
  }